function imgFx(imgFx) {

  switch(imgFx) {
  case 0: 
    filter(THRESHOLD, map(px, 0, windowWidth, 0.16, 0.83));
    break;
    //======================================================= 
  case 1:
    filter(GRAY);
    break;
    //======================================================= 
  case 2:
    filter(INVERT);
    break;
    //======================================================= 
  case 3:
    filter(POSTERIZE, map(px, 0, windowHeight, 2, 10));
    break;
    //======================================================= 
  case 4:
    filter(BLUR, map(py, 0, windowHeight, 1.5, 3));
    break;
    //======================================================= 
  case 5:
    filter(ERODE);
    break;
    //======================================================= 
  case 6:
    filter(DILATE);
    break;
    //======================================================= 
  case 7:
    filter(blur);
    break;
    //======================================================= 
  case 8:
    break;
    //=======================================================
  }
}
